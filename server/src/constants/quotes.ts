export const getTodaysQuote = (seed: string): MotivationalQuote => {
  if (!MOTIVATIONAL_QUOTES.length) {
    return { id: "fallback", text: "Stay consistent today.", author: "AHAR" };
  }

  const hash = seed
    .split("")
    .reduce((acc, char) => acc + char.charCodeAt(0), 0);
  const index = hash % MOTIVATIONAL_QUOTES.length;
  return MOTIVATIONAL_QUOTES[index] as MotivationalQuote;
};
